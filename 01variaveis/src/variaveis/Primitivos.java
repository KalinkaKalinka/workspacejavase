package variaveis;

public class Primitivos {

	public static void main(String[] args) {
		/* Hierarquia (do menor para o maior)
		 * Boolean = logico (true/false)
		 * char = 1 caracter 'h'
		 * 
		 * Para inteiros
		 * byte
		 * short
		 * int
		 * long
		 * 
		 * Para numeros reais - qq numero com casa decimal o
		 *  java interpreta como double
		 * 
		 * float
		 * double
		 * cast = processo de conversao entre dados primitivos (maior => menor)
		 */
		float valor = 1000;
		float desconto = valor * (float) 2;
		System.out.println(desconto);
		
		

	}

}
